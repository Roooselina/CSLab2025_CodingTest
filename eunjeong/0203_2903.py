# 문제 요약
# 문제 링크: https://www.acmicpc.net/problem/2903
# 중앙 이동 알고리즘을 이용하여 점의 개수를 구하는 프로그램을 작성하시오.
#
# [알고리즘 설명]
# 1. 초기 상태에서 정사각형을 이루는 점 4개가 주어진다.
# 2. 이후 각 단계마다 다음 과정을 수행한다.
#    - 정사각형의 각 변의 중앙에 점을 하나 추가한다.
#    - 정사각형의 중심에 점을 하나 추가한다.
# 3. 매 단계마다 점의 개수가 증가하며, N번째 단계에서 점의 개수를 구해야 한다.
#
# [입력]
# - 첫째 줄에 정수 N이 주어진다. (1 ≤ N ≤ 15)
#
# [출력]
# - N단계 후 점의 개수를 출력한다.

print((2**int(input())+1)**2) 